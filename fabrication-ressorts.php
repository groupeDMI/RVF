<h2>Ressorts de compression</h2>
<div class="divd"><a href="ressorts/ressort-compression-big.jpg"><img src="ressorts/ressort-compression.jpg" rel="zoom:651,433,ressort" border="0" alt="" /></a><br /><div class="legende">Ressorts compression<br />Zoom sur l'image</div></div>
<p>
Le <em>ressort de compression</em> le plus populaire est le ressort amortisseur. RVF peut produire des ressorts de compression à partir d'un diamètre de fil de 0,2mm à 2 mm. Nous pouvons réaliser un meulage ainsi que des pré-conformation si nécessaire.
Le plus courant des ressorts, enroulé à droite ou à gauche, il est le ressort le plus fréquemment utilisé dans l'industrie avec des formes classiques, coniques ou biconiques, allant du fil de diamètre 0,08 au 2mm. Le ressort de compression peut être décliné à l'infini avec des traitements de surface adaptés améliorant son alimentation automatique sur votre chaine de montage. Afin d'obtenir des performances supérieures, il peut être meulé , subir un grenaillage ou encore une préconformation.
</p>

<div class="nbsp">&nbsp;</div>

<h3>Ressorts de torsion et double torsion</h3>
<div class="divg"><a href="ressorts/ressort-torsion-big.jpg"><img src="ressorts/ressort-torsion.jpg" rel="zoom:651,433,ressort" border="0" alt="" /></a><br /><div class="legende">Ressorts torsion<br />Zoom sur l'image</div></div>
<p>
RVF réalise également des <em>ressorts torsion</em> et double torsion à partir d'un diamètre de fil de 0,2 à 2mm.
Représenté par le ressort de pince à linge, le ressort de torsion et le ressort double torsion peut être fabriqué par RVF avec un diamètre de fil allant de 0,1 à 2mm. Les branches peuvent être de formes complexes afin de garantir un accrochage optimal dans le sous-ensemble le respect du couple et notre préoccupation majeure dans la programmation de nos machines à commande numérique. Nous pouvons améliorer l'absence de bruit en fonctionnement grâce à un procédé interne de nettoyage par ultrason et pose d'un revêtement adapté sur le ressort.
</p>

<div class="nbsp">&nbsp;</div>

<h4>Pièce en fil formé</h4>
<div class="divd"><a href="ressorts/fil-forme-big.jpg"><img src="ressorts/ressort-fil-forme.jpg" rel="zoom:651,433,ressort" border="0" alt="" /></a><br /><div class="legende">Ressorts fil formés<br />Zoom sur l'image</div></div>
<p>
Les techniciens et ingénieurs de RVF produisent par ailleurs des <em>pièces en fil formé</em> à partir d'un diamètre de fil de 0,2 mm à 2mm.

Le fil formé toujours en acier inoxydable ou en acier au carbone s'approche de la famille des tringles et anneaux par exemple : les tiges d'ouverture des capots moteurs, les tringleries de serrures de voiture ou encore le support d'une ampoule de voiture ou un simple trombone. De formes diverses, nous pouvons réaliser votre pièce idéale à partir de fil de 0,1 à 2 mm avec le traitement de surface que vous souhaitez.

<br /><span class="italic">Pièces en fil formé <a href="#" onmouseover="javascript:more('div2')" onmouseout="javascript:more('div2')"><img src="ressorts/profane.png" border="0" alt="Plus d'info" /></a></span>
</p>
<p class="expandable" id="div2" class="nbsp" style="">
<strong>Pièce en fil formé :</strong><br />
Il s'agit du même fil qu'un ressort traditionnel mais mis en forme sans limites : <br />exemple un trombone, une attache de conserve en verre, le support d'une ampoule de voiture, la pièce en métal qui freine un ski lors d'une chute...
</p>

