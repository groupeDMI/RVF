<h1>Ressorts de traction</h1>
<div class="divg"><a href="ressorts/ressort-traction-big.jpg"><img src="ressorts/ressort-traction.jpg" rel="zoom:651,433,ressort" border="0" alt="" /></a><br /><div class="legende">Ressorts traction<br />Zoom sur l'image</div></div>
<p>
RVF fabrique des ressorts de traction à partir d'un diamètre de fil de 0,1 mm à 2 mm. Véritable moteur d'une fonction mécanique,le ressort de traction peut chez RVF être réalisé à partir d'un fil acier inoxydable ou au carbone d'un diamètre de fil compris entre 0,1 et 2 mm.
Les boucles sont anglaises, allemandes ou encore de formes diverses. Face à la corrosion, nous pouvons proposer un traitement de surface adapté en terme de glisse  et de bruits. De la petite à la très grande série, nous sommes à votre disposition pour créer le ressort de traction idéal.
<br />
