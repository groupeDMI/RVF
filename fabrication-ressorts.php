</p>
<p class="expandable" id="div1">
<strong>Les oeillets d'un ressort :</strong><br />
Il s'agit de la forme des boucles aux extrémités ;<br />Elles peuvent être soit une spire ou une demi spire relevée ou formée sans limites.<br /><br />
</p>
 
