<div class="decale">
<p class="expandable" id="div1">
<strong>Nettoyage par ultrasons :</strong><br />
Nous disposons d'une chaine de nettoyage à procédé lessiviel associé à des ultrasons afin d'obtenir une parfaite propreté conforme aux exigences de nos clients automobile, cosmétique et médical.
</p>
<p class="expandable" id="div2">
<strong>Conditionnement spécifique :</strong><br />
Nous adaptons le conditionnement au moyen de distribution sur la chaine de montage du client, le plus répandu est le plateau alvéolé qui permet de prendre les ressorts un par un mais aussi les chargements en bande plastique, en tuyau Pvc, en sachets anti corrosion pour les ressorts en carbonne. Nous conditionnons aussi nos ressorts sur des tiges afin d'éviter tout contact et de garder les caractéristiques techniques intactes.
</p>
<p class="expandable" id="div3">
<strong>Traitement anti-bruit :</strong><br />
Principalement demandé par l'industrie automobile, le traitement de surface anti-bruit sert à atténuer le bruit de frottement du ressort avec son environnement par exemple: les poignées intérieures de voiture, le système d'ouverture des boites à gants, le bouton poussoir du frein à main, le traitement remplace la bonne vieille goutte d'huile ou la graisse qui a le désavantage de disparaitre avec le temps alors que le traitement de la pièce demeure.
</p>
<p class="expandable" id="div4">
<strong>Traiment de glisse :</strong><br />
Il va souvent de pair avec le traitement anti bruit il permet aux ressorts de se démêler facilement mais surtout d'améliorer la distribution automatique sur la chaine d'assemblage.
</p>
<p class="expandable" id="div5">
<strong>Tribofinition :</strong><br />
Il s'agit d'un polissage du ressort par effet de vibration associé à des particules abrasives. Cela permet un polissage et un ébavurage des ressorts en une seule opération.
</p>
<p class="expandable" id="div6">
<strong>Grenaillage :</strong><br />
Il s'agit d'une projection de microbilles d'aciers sur le ressort afin d'améliorer les caractéristiques mécaniques de celui-ci ainsi que son endurance.
</p>
<p class="expandable" id="div7">
<strong>Meulage d'un ressort :</strong><br />
Il s'agit d'aplanir les deux faces d'un ressort de compression à l'aide de meules abrasives, ce qui entraine une amélioration de la perpendicularité avec le corps ainsi qu'une amélioration du parallélisme des extrémités.
</p>
<p class="expandable" id="div8">
<strong>Microbillage :</strong><br />
Même opération et mêmes objectifs que le grenaillage mais le traitement du ressort s'effectue ici avec des billes de verres et non d'acier.
</p>
<p class="expandable" id="div9">
<strong>Traitement de surface :</strong><br />
C'est un dépôt d'une matière protectrice sur le ressort afin d'améliorer sa résistance à la corrosion, mais il peut s'agir également de pouvoir différencier un ressort par rapport à un autre à l'aide de deux couleurs.
</p>
