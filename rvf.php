<li>Proposition de conditionnements spécifiques adaptés aux besoins d'industrialisation de nos clients</li>
<li>Intégration du ressort dans son proche environnement dans nos ateliers lorsque la fonction du ressort nécessite cette solution (qualitativement et économiquement).</li>
